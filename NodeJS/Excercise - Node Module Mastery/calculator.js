var calculate = {
  add: function(firstTerm, secondTerm) {
    return firstTerm + secondTerm;
  },
  subtract: function(firstTerm, secondTerm) {
    return firstTerm - secondTerm;
  },
  divide: function(firstTerm, secondTerm) {
    return firstTerm / secondTerm;
  },
  multiply: function(firstTerm, secondTerm) {
    return firstTerm * secondTerm;
  }
}

module.exports = calculate;
